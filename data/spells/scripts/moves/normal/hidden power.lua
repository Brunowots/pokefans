local area = createCombatArea(AREA_CIRCLE2X2)	
local damage = 50

function onCastSpell(creature, variant)
	local elements = {COMBAT_FIREDAMAGE, COMBAT_ICEDAMAGE, COMBAT_PSYCHICDAMAGE, COMBAT_GRASSDAMAGE, COMBAT_NORMALDAMAGE, COMBAT_WATERDAMAGE, COMBAT_FLYINGDAMAGE, COMBAT_POISONDAMAGE, COMBAT_ELECTRICDAMAGE, COMBAT_GROUNDDAMAGE, COMBAT_ROCKDAMAGE, COMBAT_BUGDAMAGE, COMBAT_DRAGONDAMAGE, COMBAT_GHOSTDAMAGE, COMBAT_DARKDAMAGE, COMBAT_STEELDAMAGE, COMBAT_FAIRYDAMAGE, COMBAT_FIGHTINGDAMAGE}
	doAreaCombatHealth(creature.uid, elements[math.random(1, 18)], creature:getPosition(), area, damage, damage, 581)
	return true
end